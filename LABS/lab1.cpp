#include <iostream>

using namespace std;

int main()
{
  int num1 = 0;
  int num2 = 0;

  cout << "Enter an Integer:";

  cin >> num1;

  cout << endl;

  cout<< "Enter another Integer:";

  cin>> num2;

  cout << endl;

  cout << num1 << " + " << num2 << " = " << num1+num2 << endl;

  cout<< num1 << " - " << num2 << " = " << num1-num2 << endl;  
  cout<< num1 << " * " << num2 << " = " << num1*num2 << endl;
 
}
